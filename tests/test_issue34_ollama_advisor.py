# ── Fake HTTP infrastructure ───────────────────────────────────────────────────


class _FakeHTTPResponse:
    """Minimal urllib-response stand-in (supports the context-manager protocol)."""

    def __init__(self, status: int, body: bytes) -> None:
        self.status = status
        self._body = body

    def read(self) -> bytes:
        return self._body

    def __enter__(self) -> "_FakeHTTPResponse":
        return self

    def __exit__(self, *_args: object) -> None:
        pass


class _CapturingUrlOpener:
    """Drop-in for urllib.request.urlopen that captures every call."""

    def __init__(
        self,
        response: _FakeHTTPResponse | None = None,
        side_effect: Exception | None = None,
    ) -> None:
        self.calls: list[dict] = []
        self._response = response
        self._side_effect = side_effect

    def __call__(
        self, request: object, timeout: float | None = None
    ) -> _FakeHTTPResponse:
        call_record: dict = {"timeout": timeout}
        if isinstance(request, urllib.request.Request):
            call_record["url"] = request.full_url
            call_record["data"] = request.data
            call_record["method"] = request.get_method()
        else:
            call_record["url"] = str(request)
            call_record["data"] = None
            call_record["method"] = None
        self.calls.append(call_record)

        if self._side_effect is not None:
            raise self._side_effect
        assert self._response is not None
        return self._response


# ── Response body builders ─────────────────────────────────────────────────────


def _chat_response_body(candidates: tuple[str, ...]) -> bytes:
    """Build a realistic /api/chat response.

    The model ranks candidates in REVERSED order so the test can verify the
    advisor actually parsed the response rather than returning the input order.
    """
    ranked = list(reversed(candidates))
    explanations = {
        name: f"ranked #{i + 1} by advisor" for i, name in enumerate(ranked)
    }
    content = json.dumps({"ranking": ranked, "explanations": explanations})
    envelope = {
        "model": "glm-5.2:cloud",
        "message": {"role": "assistant", "content": content},
        "done": True,
    }
    return json.dumps(envelope).encode()


def _generate_response_body(candidates: tuple[str, ...]) -> bytes:
    """Build a realistic /api/generate response (alternative endpoint per criterion)."""
    ranked = list(reversed(candidates))
    explanations = {
        name: f"ranked #{i + 1} by advisor" for i, name in enumerate(ranked)
    }
    payload = json.dumps({"ranking": ranked, "explanations": explanations})
    envelope = {
        "model": "glm-5.2:cloud",
        "response": payload,
        "done": True,
    }
    return json.dumps(envelope).encode()


