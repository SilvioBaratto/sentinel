def _make_config(
    *,
    enabled: bool = True,
    base_url: str = "http://localhost:11434",
    model: str = "glm-5.2:cloud",
    request_timeout: float = 5.0,
) -> AdvisorConfig:
    return AdvisorConfig(
        enabled=enabled,
        base_url=base_url,
        model=model,
        request_timeout=request_timeout,
    )


def _make_advisor(
    *,
    enabled: bool = True,
    base_url: str = "http://localhost:11434",
    model: str = "glm-5.2:cloud",
    request_timeout: float = 5.0,
    url_opener: object | None = None,
) -> object:
    config = _make_config(
        enabled=enabled,
        base_url=base_url,
        model=model,
        request_timeout=request_timeout,
    )
    return build_advisor(config, _url_opener=url_opener)


